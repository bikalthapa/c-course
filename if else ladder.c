// If else ladder //
#include<stdio.h>
int main(){
	int rollno = 2;
	if(rollno==1){
		printf("Your roll no is 1");
	}else if(rollno==2){
		printf("Your roll no is 2");
	}else if(rollno==3){
		printf("Your roll no is 3");
	}else if(rollno==4){
		printf("Your roll no is 4");
	}else{
		printf("I don't know who are you");
	};
	
	/*
	output:
	Your roll no is 2
	----------------------------------------
	*/
	return(0);
}
